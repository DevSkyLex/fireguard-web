/**
 * Type CalendarView
 *
 * @description
 * The presentations the {@link Calendar} can render: a month grid, a week
 * time-grid, or a chronological agenda list. Consumers may restrict the offered
 * set through the calendar configuration.
 *
 * @since 1.0.0
 */
export type CalendarView = 'month' | 'week' | 'agenda';
